#include <iostream>
using namespace std;
void rev(int num)
{
  if (num != 0)
  {
    int next;
    cin >> next;
    rev(next);
    cout << num << " ";
  }
}
int main()
{
  int num;
  cin >> num;
  rev(num);
  return 0;
}
