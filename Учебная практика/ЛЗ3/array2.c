/*Array 2. Дано целое число N (>0). Сформировать и вывести целочисленный массив размера N,
содержащий степени двойки от первой до N-й: 2, 4, 8, 16, ... .*/
#include <stdio.h>
 
int main()
{
    int a[10];
    int n, i=1;

    scanf("%d",&n);
 
    for (i = 1, a[0] = 2; i < n; ++i) 
        a[i] = a[i-1] * 2;
 
    for (i = 0; i < n; ++i) 
        printf("%d : %d\n", i , a[i]);
    return 0;
}